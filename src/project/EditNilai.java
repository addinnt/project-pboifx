    public void tampil_combo()
    {
        try {
        Connection c = KoneksiDatabase.getKoneksi();
        Statement s = c.createStatement();
        String sql = "select id from nilai order by id asc";      // disini saya menampilkan NIM, anda dapat menampilkan
        ResultSet res = s.executeQuery(sql);                                // yang anda ingin kan
        
        while(res.next()){
            Object[] ob = new Object[6];
            ob[0] = res.getString(1);
            
            jComboBoxId.addItem(ob[0]);                                      // fungsi ini bertugas menampung isi dari database
        }
        res.close(); s.close();
         
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
