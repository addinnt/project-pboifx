    public void tampil()
    {
        try {
        Connection c = KoneksiDatabase.getKoneksi();
        Statement s = c.createStatement();
        String sql = "select ipa, ips, mtk,bahasa,english from nilai where id='"+jComboBoxId.getSelectedItem()+"'";  
        ResultSet res = s.executeQuery(sql);
        
        while(res.next()){
            Object[] ob = new Object[5];
            ob[0]=  res.getString(1);//nama
            ob[1]= res.getString(2);//ipa
            ob[2]= res.getString(3);//ips
            ob[3]= res.getString(4);//bahasa
            ob[4]= res.getString(5);//english
            
            
            jTextFieldIpa.setText((String) ob[0]);
            jTextFieldIps.setText((String) ob[1]);
            jTextFieldMtk.setText((String) ob[2]);
            jTextFieldBahasa.setText((String) ob[3]);
            jTextFieldEnglish.setText((String) ob[4]);
        }
        res.close(); s.close();
         
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }              
    }
    private void DeleteData() {
    
    String sql = "DELETE FROM nilai WHERE id='" + jComboBoxId.getSelectedItem() + "'";
    try {
        Connection c = KoneksiDatabase.getKoneksi();
        Statement s = c.createStatement();
        s.executeUpdate(sql);
    } catch (Exception e) {
        System.out.println("Terjadi kesalahan, karena " + e.getMessage());
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan, karena " + e.getMessage());
    }
}
    
